inputMap = input.split("\n")

mapGrid = []
for line in inputMap:
    mapGrid.append(list(line))

guardX = 0
guardY = 0

for i in range(len(mapGrid)):
    for x in range(len(mapGrid[i])):
        if mapGrid[i][x] == "^":
            guardY = i
            guardX = x

# -------------------------
# Part 1: get the path tiles
# -------------------------
pathTiles = set()

gx = guardX
gy = guardY
direction = "^"

while True:
    pathTiles.add((gx, gy))

    if direction == "^":
        if gy - 1 < 0:
            break
        if mapGrid[gy - 1][gx] == "#":
            direction = ">"
        else:
            gy -= 1

    elif direction == ">":
        if gx + 1 >= len(mapGrid[0]):
            break
        if mapGrid[gy][gx + 1] == "#":
            direction = "v"
        else:
            gx += 1

    elif direction == "v":
        if gy + 1 >= len(mapGrid):
            break
        if mapGrid[gy + 1][gx] == "#":
            direction = "<"
        else:
            gy += 1

    elif direction == "<":
        if gx - 1 < 0:
            break
        if mapGrid[gy][gx - 1] == "#":
            direction = "^"
        else:
            gx -= 1

# -------------------------
# Part 2: test only the path tiles
# -------------------------
possiblePositions = 0

for (testX, testY) in pathTiles:

    if mapGrid[testY][testX] != ".":
        continue
    if testX == guardX and testY == guardY:
        continue

    gx = guardX
    gy = guardY
    direction = "^"

    visitedStates = set()
    looped = False

    while True:

        state = (gx, gy, direction)
        if state in visitedStates:
            looped = True
            break
        visitedStates.add(state)

        if direction == "^":
            if gy - 1 < 0:
                break

            if (gy - 1 == testY and gx == testX) or mapGrid[gy - 1][gx] == "#":
                direction = ">"
            else:
                gy -= 1

        elif direction == ">":
            if gx + 1 >= len(mapGrid[0]):
                break

            if (gy == testY and gx + 1 == testX) or mapGrid[gy][gx + 1] == "#":
                direction = "v"
            else:
                gx += 1

        elif direction == "v":
            if gy + 1 >= len(mapGrid):
                break

            if (gy + 1 == testY and gx == testX) or mapGrid[gy + 1][gx] == "#":
                direction = "<"
            else:
                gy += 1

        elif direction == "<":
            if gx - 1 < 0:
                break

            if (gy == testY and gx - 1 == testX) or mapGrid[gy][gx - 1] == "#":
                direction = "^"
            else:
                gx -= 1

    if looped:
        possiblePositions += 1

print("POSSIBLE LOOP POSITIONS: " + str(possiblePositions))